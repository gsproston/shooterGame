package shootergame;
        
public class hud {
    protected float emptyXpos, emptyYpos, textXpos, textYpos, leftXpos, leftYpos, midXpos, midYpos, rightXpos, rightYpos;
    protected String leftPath, midPath, rightPath, emptyPath, textPath;
    public hud() {
        
    }
    public float getEmptyXpos() {
        return emptyXpos;
    }
    public float getEmptyYpos() {
        return emptyYpos;
    }
    public float getTextXpos() {
        return textXpos;
    }
    public float getTextYpos() {
        return textYpos;
    }
    public String getEmptyPath() {
        return emptyPath;
    }
    public String getTextPath() {
        return textPath;
    }
    public String getLeftPath() {
        return leftPath;
    }
    public String getRightPath() {
        return rightPath;
    }
    public String getMidPath() {
        return midPath;
    }
    public float getLeftXpos() {
        return leftXpos;
    }
    public float getLeftYpos() {
        return leftYpos;
    }
    public float getRightXpos() {
        return rightXpos;
    }
    public float getRightYpos() {
        return rightYpos;
    }
    public float getMidXpos() {
        return midXpos;
    }
    public float getMidYpos() {
        return midYpos;
    }
}
